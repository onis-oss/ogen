package gen

import (
	"regexp"
	"sort"
	"strings"

	"github.com/go-faster/errors"

	"github.com/ogen-go/ogen"
	"github.com/ogen-go/ogen/gen/ir"
	"github.com/ogen-go/ogen/openapi"
	"github.com/ogen-go/ogen/openapi/parser"
)

// Generator is OpenAPI-to-Go generator.
type Generator struct {
	opt        Options
	api        *openapi.API
	operations []*ir.Operation
	securities map[string]*ir.Security
	tstorage   *tstorage
	errType    *ir.StatusResponse
	router     Router
}

// Options is Generator options.
type Options struct {
	// VerboseRoute whether to print routes.
	VerboseRoute bool
	// GenerateExampleTests whether to generate decoding tests using schema examples.
	GenerateExampleTests bool
	// SkipTestRegex is regex to skip generated tests.
	SkipTestRegex *regexp.Regexp
	// SkipUnimplemented disables generation of unimplemented Handler, like UnimplementedServer generated by gRPC.
	SkipUnimplemented bool
	// InferSchemaType enables type inference for schemas. Schema parser will try to detect schema type
	// by its properties.
	InferSchemaType bool
	// Filters contains filters to skip operations.
	Filters Filters
	// IgnoreNotImplemented contains ErrNotImplemented messages to ignore.
	IgnoreNotImplemented []string
	// NotImplementedHook is hook for ErrNotImplemented errors.
	NotImplementedHook func(name string, err error)
}

// Filters contains filters to skip operations.
type Filters struct {
	PathRegex *regexp.Regexp
	Methods   []string
}

func (f Filters) accept(op *openapi.Operation) bool {
	if f.PathRegex != nil && !f.PathRegex.MatchString(op.Path.String()) {
		return false
	}

	if len(f.Methods) > 0 {
		for _, m := range f.Methods {
			if strings.EqualFold(m, op.HTTPMethod) {
				return true
			}
		}
		return false
	}

	return true
}

// NewGenerator creates new Generator.
func NewGenerator(spec *ogen.Spec, opts Options) (*Generator, error) {
	api, err := parser.Parse(spec, opts.InferSchemaType)
	if err != nil {
		return nil, &ErrParseSpec{err: err}
	}

	g := &Generator{
		opt:        opts,
		api:        api,
		operations: nil,
		securities: map[string]*ir.Security{},
		tstorage:   newTStorage(),
		errType:    nil,
		router:     Router{},
	}

	if err := g.makeIR(api.Operations); err != nil {
		return nil, errors.Wrap(err, "make ir")
	}

	if err := g.route(); err != nil {
		return nil, &ErrBuildRouter{err: err}
	}

	return g, nil
}

func (g *Generator) makeIR(ops []*openapi.Operation) error {
	if err := g.reduceDefault(ops); err != nil {
		return errors.Wrap(err, "reduce default")
	}

	for _, spec := range ops {
		if !g.opt.Filters.accept(spec) {
			continue
		}

		ctx := &genctx{
			path:   []string{"#", "paths", spec.Path.String(), spec.HTTPMethod},
			global: g.tstorage,
			local:  newTStorage(),
		}

		op, err := g.generateOperation(ctx, spec)
		if err != nil {
			err = errors.Wrapf(err, "path %q: %s",
				spec.Path.String(),
				strings.ToLower(spec.HTTPMethod),
			)
			if err := g.fail(err); err != nil {
				return err
			}

			continue
		}

		if err := fixEqualRequests(ctx, op); err != nil {
			return errors.Wrap(err, "fix requests")
		}
		if err := fixEqualResponses(ctx, op); err != nil {
			return errors.Wrap(err, "fix responses")
		}

		if err := g.tstorage.merge(ctx.local); err != nil {
			return err
		}

		g.operations = append(g.operations, op)
	}

	sort.SliceStable(g.operations, func(i, j int) bool {
		a, b := g.operations[i], g.operations[j]
		return strings.Compare(a.Name, b.Name) < 0
	})

	return nil
}

// Types returns generated types.
func (g *Generator) Types() map[string]*ir.Type {
	return g.tstorage.types
}

// API returns api schema.
func (g *Generator) API() *openapi.API {
	return g.api
}
